#include <unistd.h>

void	ft_putchar(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
}

int	main(int argc, char *argv[])
{
	argc--;
	while (argc)
	{
		ft_putchar(argv[argc]);
		ft_putchar("\n");
		argc--;
	}
}
